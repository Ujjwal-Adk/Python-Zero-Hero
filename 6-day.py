# Welcome to day 6 lets learn about the for loops today 

for i in range(5): # so a little bit twist is if range is till 5 then it will only print upto 5 
    print(i)

print("Loop Sucessfully executed !! ")

# So you can print like this also giving a certain range 

for i in range (1,10): #(x number to y-1)
    print(i)

print("loop sucessfully executed ! ")

# So we also can give the incerement while making the loops
for i in range(1,10,2): #(x,y-1,increment)
    print(i) 

print("loop sucesfully executed !! ")

# counting backwards 
for i in range (10,0,-1):
    print(i)

